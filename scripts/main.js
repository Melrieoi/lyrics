// shows all bands
fetch("bands.json") // This file is auto-generated by Actions
  .then(response => response.json())
  .then(bands => {
      let container = document.getElementById("bandsContainer");
      bands.forEach(band => {
          let div = document.createElement("div");
          div.innerHTML = 
            fetch(`../lyrics/Bands/${band}.json`)
                .then(response => response.json())
                .then(data => {
                    `<img src="../lyrics/${data.image}">`
                });

          `<a href="band.html?band=${band.filename}">${band.bandName}</a>`;
          container.appendChild(div);
      });
  });